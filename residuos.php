				<p>Desde 2009, o Programa Novo Óleo vem promovendo a conscientização e a sensibilização ambiental da comunidade local sobre a destinação adequada dos resíduos de óleo de cozinha.</p>
				
				<p>O óleo de cozinha já utilizado contamina a água se descartado de maneira incorreta. Esse programa da Fiagril busca esclarecer a população, orientar e promover a forma correta de armazenagem e de descarte desse resíduo tão prejudicial ao meio ambiente. Ao adotar esse programa, a Fiagril contribui diretamente com a redução dos custos de manutenção da rede de esgoto, promove a redução da poluição nos mananciais hídricos do município e mobiliza a população através de palestras educativas para uma ação concreta de preservação do meio ambiente.</p>
				
				<p>A próxima etapa é desenvolver a forma mais adequada para o gerenciamento ambiental dos resíduos sólidos de cada filial, considerando as particularidades de cada unidade, em seus diferentes desdobramentos, tais como o plano de gerenciamento de resíduos sólidos industriais (PGRSI), a coleta seletiva, a educação ambiental, a pesquisa científica e tecnológica e a avaliação de impactos ambientais.</p>

				<p>A cada dois litros de óleo entregues, o usuário recebe um cupom. Ao completar três cupons, o usuário pode trocar por um litro de óleo vegetal novo nos pontos credenciados pelo programa. Os cupons são preenchidos com os dados pessoais do usuário, que concorre a sorteio de prêmios no final do ano. Os estabelecimentos comerciais que aderem ao programa recebem tambores de vinte e cinco litros e funil para coleta. A cada dois tambores entregues, o estabelecimento recebe um frasco de produto de limpeza de cinco litros.</p>

				<p>O óleo recolhido é reciclado no processo de produção de biodiesel. O programa premia a responsabilidade socioambiental dos cidadãos, e ao mesmo tempo difunde os cuidados com o meio ambiente.</p>

				<p>Em 2015 o Novo Óleo esteve presente nos municípios de Lucas do Rio Verde, Sinop, Nova Mutum e Sorriso e recolheu 198.000 litros. A meta é recolher 204.000 litros nos quatro municípios participantes já no próximo ano e chegar a 224.910 litros coletados em 2018.</p>
