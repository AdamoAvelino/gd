			<p>A Fábrica de Biodiesel possui um Programa de Gerenciamento de Resíduos Sólidos (PGRS) que monitora todo o processo de gerenciamento dos resíduos, desde a identificação de sua geração, passando pela separação, pelo armazenamento e pelo transporte correto dos resíduos perigosos e não perigosos até a sua destinação final.</p>

			<p>A destinação dada a esses resíduos é determinada conforme seu tipo e conduzida em consonância com o estabelecido na Política Nacional de Resíduos Sólidos (PNRS). Os resíduos podem, então, ser reciclados por recicladoras locais, ou refinados, incinerados ou aterrados, sempre por empresas especializadas e  habilitadas para esses serviços.</p>

			<p>A destinação final dos resíduos perigosos nas filiais é organizada pelo setor de Sustentabilidade, que envia os resíduos contaminados e óleo lubrificante para disposição final ambientalmente adequada desses rejeitos. Quando disponível nos municípios, os demais tipos de resíduos são destinados para reciclagem ou para o aterro municipal.</p>
			
			<p>Ações pontuais de não geração, redução, reutilização e reciclagem de materiais utilizados na rotina de trabalho das unidades, como copos para consumo de água, por exemplo, são desenvolvidas para disseminar entre os colaboradores a importância da adoção de hábitos de consumo sustentável. Dessa forma, mostramos que existe valor econômico no que pode ser reciclado ou reaproveitado, e que esse tipo de atitude pró-ativa em relação aos resíduos deve ser constante na vida corporativa e além dela.</p>

			<p>Nosso objetivo é que até o ano de 2020 tenhamos implantado em todas as filiais o PGRS para controle e identificação do total de resíduos gerados pela organização. Buscamos identificar objetivos concretos para a não geração, redução, reutilização, reciclagem e disposição final ambientalmente adequada dos rejeitos. Ao atingirmos essa meta, passaremos para o passo seguinte, de determinar estratégias para a minimização de resíduos, formas de priorizar a reutilização e reciclagem e uso eficiente dos recursos não renováveis.</p>
